def are_equal(a, b):
    """Return True if a and b are equal, else False."""
    return a == b


def are_not_equal(a, b):
    """Return True if a and b are not equal, else False."""
    return a != b


def is_greater(a, b):
    """Return True if a is greater than b, else False."""
    return a > b


def is_less(a, b):
    """Return True if a is less than b, else False."""
    return a < b


def is_greater_or_equal(a, b):
    """Return True if a is greater than or equal to b, else False."""
    return a >= b


def is_less_or_equal(a, b):
    """Return True if a is less than or equal to b, else False."""
    return a <= b
